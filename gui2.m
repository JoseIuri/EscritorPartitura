tempo = tempom*60 + tempos;

set(findobj(gcf,'Tag','gravar'),'String', 'Gravando');

nbits = 16;
dev_id = getfield(getfield(audiodevinfo, 'input'), 'ID');

arec = audiorecorder;

recordblocking(arec, tempo);

data = getaudiodata(arec);
nom = get(findobj(gcf, 'Tag','nomearq'),'String');
nomf = strcat(nom,'.wav');

audiowrite(nomf,data, 11025);
figure(2);
converte_musica(nomf);


